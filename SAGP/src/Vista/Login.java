    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        Usuario u= new Usuario();
        u.setUsuario(txtUusario.getText().trim());
        u.setContra(txtPassword.getText());;
        usuarioController controller = new usuarioController();
        if(controller.validarLogin(u)){
           Menu m = new Menu();
           m.setVisible(true);
           dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Usuario y/o Contrase√±a incorrecta", "Error de Logeo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

